        var pmfFileupload = {
            init: function() {
                this.cacheDom();
                this.events();
            },
            cacheDom: function() {
                this.$filePlaceholder = $('.updatePdf');
                this.$fileUpload = this.$filePlaceholder.find('.fileUpload');
                this.$fileBrowseTxt = this.$filePlaceholder.find('.file-browse-txt span');
            },
            events: function() {
                this.$fileUpload.on('change', this.getFileName.bind(this));
            },
            getFileName: function() {
                this.newVal = this.$fileUpload.val().replace(/C:\\fakepath\\/i, '');
                if (this.newVal !== "") {
                    $('.file-browse-txt span').text(this.newVal)
                    $('.file-browse-txt').addClass('hasValue')
                } else {
                    $('.file-browse-txt span').text("請上傳JPG/JPEG格式之檔案")
                    $('.file-browse-txt').removeClass('hasValue')
                }
            }
        };
        $(document).ready(function() {
            pmfFileupload.init();
        });
        function fileRemove(){
            $('#file').val('')
            $('.file-browse-txt span').text("請上傳JPG/JPEG格式之檔案")
            $('.file-browse-txt').removeClass('hasValue')
            $('.file-browse-txt').css('color', '#BCBCBC')
        }
        function filefujianChange(target) {
            var isIE = /msie/i.test(navigator.userAgent) && !window.opera; 
            var fileSize = 0;
            if (isIE && !target.files) {
                var filePath = target.value;
                var fileSystem = new ActiveXObject("Scripting.FileSystemObject");
                var file = fileSystem.GetFile(filePath);
                fileSize = file.Size;
            } else {
                fileSize = target.files[0].size;
            }
            var size = fileSize / 1024;
            if (size > 20000) {
                // alert("附件不能大於20MB");
                target.value = "";
                return
            }
            var name = target.value;
            var fileName = name.substring(name.lastIndexOf(".") + 1).toLowerCase();
            if (fileName !="jpg" && fileName !="jpeg") {
                // alert("請選擇圖片格式文件上傳(jpg,png,gif,dwg,pdf,gif等)！");
                $('.file-browse-txt').css('color', '#BCBCBC')
                target.value = "";
                return
            }
            $('.file-browse-txt').css('color', '#000')
        }
        
        
